#!/bin/sh
# Replaces "Message recieved from..." with newlines

NAME=`who | grep tty7 | awk '{print $1}' | xargs echo -n`
TTY=tty7
echo $TTY
echo $NAME
echo -e '\n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n \n ' | write $NAME $TTY
